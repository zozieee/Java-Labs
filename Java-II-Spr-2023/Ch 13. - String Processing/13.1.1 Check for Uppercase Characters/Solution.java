import java.util.*;

public class Solution {
    public static void main(String[] args) {
        Scanner input= new Scanner(System.in);
        String S= input.nextLine();
        String test=S.toUpperCase();
        
        if (S.equals(test)){
            System.out.print("Yes");
            }
        else{
            System.out.print("No");
            }            
        }
}